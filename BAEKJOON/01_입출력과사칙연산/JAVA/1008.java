// 두 정수 A와 B를 입력받은 다음, A-B를 출력하는 프로그램을 작성하시오.
import java.util.*;
public class Main{
	public static void main(String args[]){
		Scanner sc = new Scanner(System.in);
		int firstNum, secondNum;
		firstNum = sc.nextInt();
		secondNum = sc.nextInt();
		sc.close();
		System.out.println(firstNum - secondNum);
	}
}